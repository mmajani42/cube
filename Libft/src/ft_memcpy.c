#include "../inc/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	void	*tmp;

	tmp = dest;
	if (src == NULL && dest == NULL)
		return (dest);
	while (n)
	{
		*((char *)dest) = *((char *)src);
		dest++;
		src++;
		n--;
	}
	return (tmp);
}
